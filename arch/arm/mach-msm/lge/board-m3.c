static int  msm_hsusb_vbus_init(int on)
{
	int rc = 0;
	unsigned gpio;

	gpio = GPIO_HOST_VBUS_EN;

	if (on) {
		rc = gpio_request(gpio, "i2c_host_vbus_en");
		if (rc < 0) {
			pr_err("failed to request %d GPIO\n", gpio);
			return rc;
		}
		gpio_direction_output(gpio, 1);
	} else {
		gpio_free(gpio);
	}

	return rc;
}
